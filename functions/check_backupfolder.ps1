Function New-BackupFolder {
    $path = "$currentdir\backups" 
    If (Test-Path $path) { 
        Write-Host '****   Backup folder exists!   ****' -F Y -B Black
    } 
    Else {  
        Write-Host '****   Creating backup folder   ****' -F M -B Black
        New-Item  "$currentdir\" -Name "backups" -ItemType "directory"
    }
}