Function Get-MCRconCheck {
    $path = "$mcrcondirectory\"
    $patha = "$mcrcondirectory\mcrcon.exe" 
    If ((Test-Path $path) -and (Test-Path $patha)) { 
        Write-Host '****   mcrcon already downloaded!   ****' -F Y -B Black
    } 
    Else {  
        install-mcrcon
    }
}